package org.example.basic;

public class Bit {
    public static void main(String[] args) {
        int x = 160;
        System.out.println(x);
        x = x>>1;
        System.out.println(x);
        x = x>>1;
        System.out.println(x);
        x = x<<1;
        System.out.println(x);
        x = x<<1;
        System.out.println(x);
    }
}
